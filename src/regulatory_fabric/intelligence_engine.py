    def analyze_regulation(self, regulation_id: str) -> Dict[str, Any]:
        """Analyze a regulation and generate insights.
        
        Args:
            regulation_id: Regulation to analyze
            
        Returns:
            Analysis results
        """
        regulation = self.store.get_regulation(regulation_id)
        if not regulation:
            return {"error": "Regulation not found"}
        
        controls = self.store.get_controls_for_regulation(regulation_id)
        assessments = self.store.list_assessments(regulation_id=regulation_id)
        risks = self.store.list_risks(regulation_id=regulation_id)
        
        # Calculate coverage metrics
        total_requirements = len(regulation.get("requirements", []))
        mapped_controls = len(controls)
        coverage = (mapped_controls / total_requirements * 100) if total_requirements > 0 else 0
        
        # Calculate compliance rate
        compliant_controls = len([c for c in controls if c.get("status") == "COMPLIANT"])
        compliance_rate = (compliant_controls / len(controls) * 100) if controls else 0
        
        # Risk assessment
        risk_exposure = sum(r.get("risk_score", 0) for r in risks)
        
        return {
            "regulation_id": regulation_id,
            "regulation_name": regulation.get("name"),
            "requirements_count": total_requirements,
            "mapped_controls": mapped_controls,
            "coverage_percentage": round(coverage, 2),
            "compliance_rate": round(compliance_rate, 2),
            "assessments_count": len(assessments),
            "open_risks": len([r for r in risks if r.get("mitigation_status") == "OPEN"]),
            "risk_exposure": round(risk_exposure, 4),
            "recommendations": self._generate_recommendations(regulation, controls, compliance_rate),
        }

    def _generate_recommendations(self, regulation: Dict, controls: List[Dict], compliance_rate: float) -> List[str]:
        """Generate recommendations based on analysis.
        
        Args:
            regulation: Regulation data
            controls: Related controls
            compliance_rate: Current compliance rate
            
        Returns:
            List of recommendations
        """
        recommendations = []
        
        if compliance_rate < 50:
            recommendations.append("Critical: Immediate remediation required for compliance gaps")
        elif compliance_rate < 80:
            recommendations.append("High: Address remaining compliance gaps within 30 days")
        
        unmapped = [r for r in regulation.get("requirements", []) if not any(
            c.get("mapping", {}).get("requirement_id") == r.get("id") for c in controls
        )]
        if unmapped:
            recommendations.append(f"Map {len(unmapped)} unmapped requirements to controls")
        
        return recommendations

    def get_alerts(self, severity: Optional[str] = None, limit: int = 100) -> List[Dict[str, Any]]:
        """Get recent intelligence alerts.
        
        Args:
            severity: Optional severity filter
            limit: Maximum number of alerts to return
            
        Returns:
            List of alerts
        """
        alerts = self._alerts
        if severity:
            alerts = [a for a in alerts if a.severity == severity]
        return [
            {
                "alert_id": a.alert_id,
                "alert_type": a.alert_type,
                "severity": a.severity,
                "title": a.title,
                "description": a.description,
                "regulation_id": a.regulation_id,
                "source": a.source,
                "generated_at": a.generated_at.isoformat(),
            }
            for a in alerts[-limit:]
        ]

    def predict_compliance_impact(self, regulatory_update: Dict[str, Any]) -> Dict[str, Any]:
        """Predict the compliance impact of a regulatory update.
        
        Args:
            regulatory_update: The regulatory update to analyze
            
        Returns:
            Impact prediction
        """
        affected_reqs = regulatory_update.get("affected_requirements", [])
        impact_level = regulatory_update.get("compliance_impact", "MEDIUM")
        
        # Calculate affected controls
        affected_controls = 0
        for mapping in self.store.control_mappings.values():
            if mapping.get("requirement_id") in affected_reqs:
                affected_controls += 1
        
        # Estimate remediation effort
        effort_map = {"LOW": 1, "MEDIUM": 3, "HIGH": 5, "CRITICAL": 8}
        estimated_days = effort_map.get(impact_level, 3) * max(1, affected_controls)
        
        return {
            "update_id": regulatory_update.get("update_id"),
            "affected_requirements": len(affected_reqs),
            "affected_controls": affected_controls,
            "impact_level": impact_level,
            "estimated_remediation_days": estimated_days,
            "priority": "CRITICAL" if affected_controls > 10 else impact_level,
            "required_actions": self._generate_required_actions(regulatory_update),
        }

    def _generate_required_actions(self, update: Dict[str, Any]) -> List[str]:
        """Generate required actions for an update.
        
        Args:
            update: Regulatory update
            
        Returns:
            List of required actions
        """
        actions = []
        actions.append("Review updated requirements")
        actions.append("Assess impact on existing controls")
        
        if update.get("effective_date"):
            days_until = (update["effective_date"] - datetime.now(timezone.utc)).days
            if days_until > 0:
                actions.append(f"Plan remediation before {days_until} days")
        
        return actions


