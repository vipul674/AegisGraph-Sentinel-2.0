"""
Regulation Intelligence Engine for Regulatory Fabric.

Monitors regulatory sources, tracks changes, and provides intelligent analysis.
"""

from datetime import datetime, timezone, timedelta
from typing import Dict, List, Optional, Any, Callable
from dataclasses import dataclass, field
import threading
import hashlib


@dataclass
class IntelligenceConfig:
    """Configuration for the intelligence engine."""
    update_check_interval_hours: int = 24
    max_sources: int = 100
    retention_days: int = 365
    enable_auto_processing: bool = True
    confidence_threshold: float = 0.7


@dataclass
class IntelligenceAlert:
    """Alert generated by the intelligence engine."""
    alert_id: str
    alert_type: str
    severity: str
    title: str
    description: str
    regulation_id: Optional[str]
    source: str
    generated_at: datetime = field(default_factory=lambda: datetime.now(timezone.utc))
    metadata: Dict[str, Any] = field(default_factory=dict)


class RegulationIntelligenceEngine:
    """AI-powered regulation intelligence engine.
    
    Monitors regulatory changes, analyzes impact, and generates alerts.
    """

    def __init__(self, store: Any, config: Optional[IntelligenceConfig] = None):
        """Initialize the intelligence engine.
        
        Args:
            store: Compliance store instance
            config: Engine configuration
        """
        self.store = store
        self.config = config or IntelligenceConfig()
        self._alerts: List[IntelligenceAlert] = []
        self._sources: Dict[str, Dict[str, Any]] = {}
        self._lock = threading.Lock()
        self._subscribers: List[Callable[[IntelligenceAlert], None]] = []

    def register_source(self, source_id: str, source_info: Dict[str, Any]) -> bool:
        """Register a regulatory information source.
        
        Args:
            source_id: Unique source identifier
            source_info: Source metadata (name, url, type, etc.)
            
        Returns:
            True if registered successfully
        """
        if len(self._sources) >= self.config.max_sources:
            return False
        
        with self._lock:
            self._sources[source_id] = {
                **source_info,
                "registered_at": datetime.now(timezone.utc),
                "last_check": None,
                "check_count": 0,
            }
        return True

    def subscribe(self, callback: Callable[[IntelligenceAlert], None]) -> None:
        """Subscribe to intelligence alerts.
        
        Args:
            callback: Function to call when alerts are generated
        """
        with self._lock:
            self._subscribers.append(callback)

    def _notify_subscribers(self, alert: IntelligenceAlert) -> None:
        """Notify all subscribers of a new alert."""
        for callback in self._subscribers:
            try:
                callback(alert)
            except Exception:
                pass

    def check_regulatory_updates(self, source_id: Optional[str] = None) -> List[Dict[str, Any]]:
        """Check for new regulatory updates from registered sources.
        
        Args:
            source_id: Optional source ID to check specific source
            
        Returns:
            List of new updates found
        """
        updates_found = []
        sources_to_check = (
            {source_id: self._sources[source_id]} if source_id and source_id in self._sources
            else self._sources
        )
        
        for src_id, source in sources_to_check.items():
            source["last_check"] = datetime.now(timezone.utc)
            source["check_count"] += 1
            
            # Simulate checking for updates (in production, this would call actual APIs)
            new_updates = self._fetch_source_updates(src_id, source)
            updates_found.extend(new_updates)
            
            for update in new_updates:
                self.store.add_regulatory_update(update)
                
                alert = IntelligenceAlert(
                    alert_id=hashlib.md5(f"{update['update_id']}".encode()).hexdigest()[:12],
                    alert_type="REGULATORY_UPDATE",
                    severity=update.get("compliance_impact", "MEDIUM"),
                    title=f"New {update.get('update_type', 'UPDATE')} for {update.get('regulation_id', 'Unknown')}",
                    description=update.get("summary", ""),
                    regulation_id=update.get("regulation_id"),
                    source=src_id,
                )
                self._alerts.append(alert)
                self._notify_subscribers(alert)
        
        return updates_found

